    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String pno= txtpno.getText();
        String splace= txtsource.getSelectedItem().toString();
        String dplace= txtdest.getSelectedItem().toString();
        String tno=txttno.getText();
        String tname=txttname.getText();
        String price= txtprice.getText();
        String ticket=txtticket.getText();
        String total=txttotal.getText();
        
        
        try {
            pst= con.prepareStatement("insert into reservation(pno,Source,Destination,tno,tname, price, ticket, tot)values(?,?,?,?,?,?,?,?)");
            pst.setString(1, pno);
            pst.setString(2, splace);
            pst.setString(3, dplace);
            pst.setString(4, tno);
            pst.setString(5, tname);
            pst.setString(6, price);
            pst.setString(7, ticket);
            pst.setString(8, total);
            int k= pst.executeUpdate();
            if (k==1)
            {
                JOptionPane.showMessageDialog(this,"Record Added");
            }
            else{
                JOptionPane.showMessageDialog(this,"Record Failed");
                
            }
           
                  
        } catch (SQLException ex) {
            Logger.getLogger(addtrain.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

