    public void LoadSource()
    {
        try
        {
            pst=con.prepareStatement("SELECT Source FROM traindetails");
            rs= pst.executeQuery();
            txtsource.removeAllItems();
            while(rs.next())
            {
                txtsource.addItem(rs.getString(1));
            }
        }
        catch(Exception e)
        {
            e.printStackTrace();
        }
    }
    
    public void LoadDestination()
    {
        try
        {
            pst=con.prepareStatement("SELECT Destination FROM traindetails");
            rs= pst.executeQuery();
            txtdest.removeAllItems();
            while(rs.next())
            {
                txtdest.addItem(rs.getString(1));
            }
        }
        catch(Exception e)
        {
            e.printStackTrace();
        }
    }
    
    public void load()
    {
        String splace=txtsource.getSelectedItem().toString();
        String dplace=txtdest.getSelectedItem().toString();
        
        try
        {
            pst=con.prepareStatement("SELECT * FROM traindetails where Source=? and Destination=?");
            pst.setString(1,splace);
            pst.setString(2,dplace );
            rs=pst.executeQuery();
            
            if(rs.next()==true)
            {
                txttno.setText(rs.getString(1));
                txttname.setText(rs.getString(2));
                txtprice.setText(rs.getString(5));
                
            }
            else
            {
                JOptionPane.showMessageDialog(this, "Record Not Found");
                
            }
        }
            catch(Exception e)
                    {
                    e.printStackTrace();
                    }
        }
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
